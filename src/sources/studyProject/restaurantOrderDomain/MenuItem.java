package sources.studyProject.restaurantOrderDomain;

/**
 * 메뉴항목 객체
 * */

class MenuItem {
    private String name;
    private int price;

    public MenuItem(String name, int price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

}
